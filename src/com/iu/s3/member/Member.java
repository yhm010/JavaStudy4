package com.iu.s3.member;

public class Member {

	private String name; // 이름
	private String email; // 이메일
	private double muge; // 몸무게
	private double ki; // 키
	private int age; // 나이
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public double getMuge() {
		return muge;
	}
	public void setMuge(double muge) {
		this.muge = muge;
	}
	public double getKi() {
		return ki;
	}
	public void setKi(double ki) {
		this.ki = ki;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}

	// setter 변수의 값을 집어넣는 것, getter 변수의 값을 외부로 가지고 나가는 것
	// set변수명, get변수명
	
	
	
	
	
	} //클래스 끝

